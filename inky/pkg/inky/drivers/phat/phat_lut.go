package phat

// LUT tables for InkyPHAT/WHAT
// These define the voltage waveforms for display refresh
// Based on Pimoroni's Python implementation

// GetLUT returns the lookup table for the provided display configuration
func GetLUT(lutType string) []byte {
	switch lutType {
	case "black":
		return lutBlack[:]
	case "red":
		return lutRed[:]
	case "red_ht":
		return lutRedHT[:]
	case "yellow":
		return lutYellow[:]
	default:
		return lutBlack[:]
	}
}

